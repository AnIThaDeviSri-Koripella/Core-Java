public class Types2 {
    public static void main(String args[])
    {
        int num1 = 10_00_000;   //java provides a feature of counting zeroes using underscore.
        int num2 = 1000000;
        double d = 56;
        double d2 = 12e10;
        char c = 'b';
        c++;
        boolean b = false;
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(d);
        System.out.println(d2);
        System.out.println(c);
        System.out.println(b);

    }
}
